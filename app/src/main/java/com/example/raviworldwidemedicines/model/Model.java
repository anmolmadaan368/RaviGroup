package com.example.raviworldwidemedicines.model;

public class Model {

    private int id;
    private int postId;
    private String src;

    public int getId() {
        return id;
    }

    public int getPostId() {
        return postId;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setPostId(int postId) {
        this.postId = postId;
    }

    public void setSrc(String src) {
        this.src = src;
    }

    public String getSrc() {
        return src;
    }
}
